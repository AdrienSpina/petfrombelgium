<h3>Comment faire le choix d'un animal de compagnie ?</h3>
<p>
Un animal de compagnie est bien plaisant, pour s'en occuper, observer ses habitudes, pouvoir le tenir et le flatter... 
C'est une <br>façon d'éloigner l'ennui en mettant un peu de vie dans la maison.
</p>
<p>
Cependant, il faut bien choisir son animal de compagnie, sinon, on risque d'avoir des surprises, 
autant du côté entretien que du <br>caractère. Au moment des déménagements, les refuges pour animaux abandonnés 
deviennent pleins à craquer, car les gens <br> abandonnent leur chiens, ou leur chats, qui ne sont pas acceptés à 
l'endroit où ils déménagent. D'autres se débarrassent de leur <br>animal domestique à cause de l'entretien qu'ils n'avaient pas bien évalué.
Donc, avant de choisir un animal, on doit savoir ce qui <br>doit être fait, pour que celui-ci soit en santé. 
Bien des animaux se laissent mourir de faim si ils sont malheureux, ou ils passent <br>leur temps couché. 
Dans le cas d'un chat, c'est normal car ils dorment près de 18 heures par jour, mais, pour un chien, 
ça peut <br>être le symptôme de maladie, ou qu'il est dépressif.
</p>
<p>
Pour éviter de rendre un animal malheureux, ou son propriétaire qui ne s'attendait pas à l'entretien 
et l'attention qu'un animal <br>domestique nécessite, on doit savoir un peu ce que l'animal a besoin, et ce qu'il fera. 
Il faut donc faire des recherches sur le <br>mode de vie de l'animal qu'on aimerait se procurer avant de l'acheter, afin de 
ne pas l'abandonner plus tard.
</p>
<p>
Naturellement, <strong>les chiens</strong> sont de très bons animaux de compagnie. 
Comme il en existe une multitude de races, on peut en obtenir un qui va mieux s'adapter à nos besoins. 
Si on désire un chien de garde, on s'en procure donc un qui est assez gros et dont le caractère est protecteur. 
D'autres aiment les petits chiens, car ils prennent moins d'espace, mais sont souvent plus nerveux aussi. 
Un chien a besoin de bouger, et on doit donc lui faire faire sa promenade, jouer à la balle pour le faire courir, 
et ce tous les jours. Il faut naturellement le faire sortir pour faire ses besoins, et cela pour 15 ans et plus. 
Si on doit se déplacer pour plusieurs jours, on doit placer le chien dans un service de garde canin. 
Il demande de l'affection, et on doit le flatter, le brosser, lui tailler les griffes, s'assurer 
qu'il ne manque pas d'eau et de nourriture. C'est donc pas mal d'entretien.
</p>
<p>
<strong>Les chats</strong> sont un peu moins difficiles d'entretien. On a simplement à les déposer dans un bac à 
litière quand on les fait entrer dans la maison pour la première fois, et ils vont habituellement 
toujours y retourner pour faire leurs besoins. Naturellement, on a pas besoin de les promener, et si 
on le laisse sortir dehors, il aura tout l'exercice dont il a besoin. Ils sont plus pantouflards, et ne 
sont pas constamment en quête d'affection comme un chien, ce qui est moins achalant pour certains. Mais, ils aiment 
bien se faire flatter de temps en temps. Si on quitte pour 2-3 jours, on peut mettre une quantité de nourriture et 
d'eau suffisante, sans devoir le mettre en hébergement.
</p>
<p>
<strong>Les lézards</strong> sont des animaux de compagnie qui sont bien souvent ennuyeux. 
Ils restent souvent en place aggripé à une branche ou au fond de leur aquarium. Selon le type de lézard, 
il peut manger des proies vivantes, et on doit souvent en élever aussi. Comme c'est un animal qui ne bouge 
pas beaucoup, on va souvent finir par trouver que son entretien est la seule chose qu'il nous apporte.
</p>
<p>
<strong>Les serpents</strong> sont aussi des animaux qui ne font pas grand chose de leurs journées, et on va 
souvent simplement les nourrir et nettoyer leur cage. On va habituellement devoir les nourrir 
avec des rongeurs vivants, au d'autres petits animaux, car ils ne mangent souvent que des proies 
entières. Certains sont des serpents constricteurs, mais d'autres mordent et peuvent être poison. 
Certaines compagnies d'assurances ne couvrent pas les dommages quand un reptile ou un serpent est présent dans le domicile.
</p>
<p>
Certaines espèces de <strong>rongeurs</strong> sont aussi des animaux domestiques populaires, comme le hamster, 
le cobaye, les gerbilles... surtout pour les enfants. Comme ce sont des rongeurs, il faut s'assurer 
d'avoir une cage bien solide pour qu'ils ne puissent pas s'en échapper. Bien des espèces sont nocturnes, et 
ils feront donc du bruit la nuit, en faisant tourner leur roue pour courir, ou à se promener et grimper dans leurs cages. 
Comme la litière est habituellement faite de copeaux de bois, qui absorbe l'urine, on va presque toujours en sentir l'odeur 
dans la maison. Si on a un mâle et une femelle, leur cycle de reproduction est très rapide et on va vite se retrouver 
avec plusieurs générations de ses petits animaux.
</p>
<p>
<strong>Les oiseaux domestiques</strong> sont de bons petits animaux de compagnie. 
Ils dorment habituellement tous la nuit, mais se lèvent avec la levée du jour 
pour se mettre à chanter. Selon l'espèce, le chant sera plaisant, comme pour les canaris, ou ils pousseront 
des cris irritants comme le cockatiel, les perruches, et bien des espèces de perroquets. 
Certains oiseaux vivent aussi longtemps, ou même plus longtemps que nous, comme le perroquet gris d'afrique, ce qui 
signifie qu'on l'aura pour la vie et même le laisser en héritage. Les oiseaux font leurs besoins où ils se trouvent, et si 
on les laisse se promener dans la maison, on va trouver des excréments un peu partout ou ils se perchent. Ils on la mauvaise 
habitude de brasser leur nourriture violemment pour trouver des graines qui leur plaisent plus, et ils en lancent donc partout autour de leur cage.
</p>
<p>
<strong>Les poissons</strong> sont un autre type d'animal de compagnie qui n'est pas trop tannant, mais qui a besoin 
de soins, de nourriture, et d'un bon nettoyage d'aquarium périodique. Selon l'espèce, on doit utiliser 
une nourriture à poisson spécifique, une température d'eau constante... S'il est carnivore, comme l'Oscar, on 
doit souvent le nourrir avec des poissons rouges vivants. Comme on doit aérer l'eau, l'aquarium est muni d'un 
filtreur, qui va constamment produire un bruit de bouillonnement, que plusieurs trouvent plutôt reposant. Naturellement, on 
ne peut que les observer, et admirer leurs couleurs et formes.
</p>
<p>
Selon le temps libre qu'on a, la patience, et même l'utilité pour un animal de compagnie, il y aura toujours 
une part d'entretien qui s'y rattache. Si on n'aime pas sortir tous les jours pour promener le chien, <strong>il est préférable 
de ne pas en avoir</strong>. Si on aime pas avoir de poils dans la maison, on ne se procurera pas un chat. Si on a bien consulté 
les informations sur une espèce d'animal, sa vie, son entretien... et qu'on trouve qu'il serait acceptable, on peut vérifier 
les centres d'adoptions d'animaux de compagnie ou les élevages de particuliers avant d'en acheter un.
</p>
