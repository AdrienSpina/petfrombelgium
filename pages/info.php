<h4>A quoi sert le site PETfromBELGIUM ?</h4>
<p>
	PETfromBELGIUM est un site qui permet d'indexer les éleveurs d'animaux de compagnie.<br>
	Ce site sert de relais entre les éleveurs et les particuliers.
</p>
<h4>S'inscrire sur PETfromBELGIUM est-il payant ?</h4>
<p>
	Non ! Tout est gratuit ! Nous sommes rémunérés grâce aux pubs présentes sur le site.
</p>
<h4>Pourquoi est-il interessant de m'inscrire en tant qu'éleveur ?</h4>
<p>
En tant qu'éleveur, il est intéressant pour vous de vous inscrire ici car PETfromBELGIUM tente de regrouper<br>
l'ensemble des éleveurs d'animaux de compagnies en Belgique pour permettre aux particuliers de trouver<br>
plus facilement l'animal de leurs rêves.
Qui plus est, il est interessant pour ceux qui veulent de devenir éleveurs <br>de pouvoir contacter d'autres éleveurs
afin de recevoir des conseils et aussi de voir les prix de certaines races d'animaux pour <br>se faire une idée de
ce qu'ils vont proposer.
Nous utilisons également vos informations dans un but démographique <br>(races d'animaux plus présentes dans certaines régions)
et UNIQUEMENT dans ce but. Vos informations personnelles <br>ne seront jamais utilisées ailleurs.
</p>
<h4>Puis-je faire confiance aux éleveurs inscrits sur ce site ?</h4>
<p>
Nous ne pouvons pas vous assurez de l'honneteté des éleveurs inscrit sur ce site.<br>
PETfromBELGIUM sert juste de relais entre les éleveurs et les particuliers.<br>
C'est à vous de prendre contact avec les éleveurs et de vous assurez de leur bonne volonté.<br>
Néanmoins, si vous remarquez des traces de violences envers les animaux ou une hygiène déplorable<br>
chez un éleveur, n'hesitez pas à nous contacter sur le site ou contacter directement la police.
</p>
<h4>Comment puis-je contacter PETfromBELGIUM ?</h4>
<p>
	Sur chaque bas de page du site, vous trouverez le bouton "CONTACTER", c'est aussi simple que ça !
</p>
